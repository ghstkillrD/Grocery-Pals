    private void add_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_add_btnMouseClicked
        // category add
        if(catID_txt.getText().isEmpty()||catName_txt.getText().isEmpty()||catDesc_txt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Fields cannot be EMPTY!", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
            conn = DriverManager.getConnection("jdbc:derby://localhost:1527/supermarketDB", "User_1", "1234");
            PreparedStatement ADD = conn.prepareStatement("INSERT INTO CATEGORIES VALUES (?,?,?)");
            ADD.setInt(1, Integer.valueOf(catID_txt.getText()));
            ADD.setString(2, catName_txt.getText());
            ADD.setString(3, catDesc_txt.getText());
            int row = ADD.executeUpdate();
            JOptionPane.showMessageDialog(this, "Category inserted successfully!");
            conn.close();
            SelectCategory();
            }catch(Exception ex){
            ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_add_btnMouseClicked

