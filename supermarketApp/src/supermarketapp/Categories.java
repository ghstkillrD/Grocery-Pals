    private void catList_tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_catList_tblMouseClicked
        // list select
        DefaultTableModel model = (DefaultTableModel)catList_tbl.getModel();
        int Index = catList_tbl.getSelectedRow();
        catID_txt.setText(model.getValueAt(Index, 0).toString());
        catName_txt.setText(model.getValueAt(Index, 1).toString());
        catDesc_txt.setText(model.getValueAt(Index, 2).toString());
    }//GEN-LAST:event_catList_tblMouseClicked

    private void edit_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_edit_btnMouseClicked
        // category edit
        if(catID_txt.getText().isEmpty()||catName_txt.getText().isEmpty()||catDesc_txt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Fields cannot be EMPTY!", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                conn = DriverManager.getConnection("jdbc:derby://localhost:1527/supermarketDB", "User_1", "1234");
                String Query = "UPDATE User_1.CATEGORIES SET CATNAME='"+catName_txt.getText()+"'"+",CATDESC='"+catDesc_txt.getText()+"'"+"WHERE CATID="+catID_txt.getText();
                Statement EDIT = conn.createStatement();
                EDIT.executeUpdate(Query);
                JOptionPane.showMessageDialog(this, "Category Updated Successfully!");
                SelectCategory();
            }catch(Exception ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_edit_btnMouseClicked

