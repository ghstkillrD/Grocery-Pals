    private void print_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_print_btnMouseClicked
        //print bill
        try{
            if(billID_txt.getText().isEmpty()||cusName_txt.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Bill ID/Customer Name cannot be EMPTY!", "Error", JOptionPane.ERROR_MESSAGE);
            }else if(bill_txt.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "ADD a product first!", "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                try{
                    conn = DriverManager.getConnection("jdbc:derby://localhost:1527/supermarketDB", "User_1", "1234");
                    PreparedStatement ADD = conn.prepareStatement("INSERT INTO BILL VALUES (?,?,?,?,?,?,?)");
                    ADD.setInt(1, Integer.valueOf(billID_txt.getText()));
                    ADD.setString(2, cusName_txt.getText());
                    ADD.setString(3, sellName_lbl.getText());
                    ADD.setString(4, billDate_lbl.getText());
                    ADD.setString(5, billTime_lbl.getText());
                    ADD.setInt(6, Integer.valueOf(finalQty_lbl.getText()));
                    ADD.setDouble(7, Double.valueOf(finalTot_lbl.getText()));
                    int row = ADD.executeUpdate();
                    //System.out.println("Wade goda");
                    conn.close();
                }catch(Exception ex){
                    ex.printStackTrace();
                }
                bill_txt.print();
                JOptionPane.showMessageDialog(this, "Bill Printed successfully!");
                clearAll();
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_print_btnMouseClicked

