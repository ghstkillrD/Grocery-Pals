    int i = 0, finalQty = 0;
    double proTot = 0.0, finalTot = 0.0;
    
    private void add_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_add_btnMouseClicked
        //product add
        if(proName_txt.getText().isEmpty()||proQty_txt.getText().isEmpty()||proQty_txt.getText().equals("0")){
            JOptionPane.showMessageDialog(this, "Quantity cannot be NULL!", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(remQty <= Integer.valueOf(proQty_txt.getText())){
            JOptionPane.showMessageDialog(this, "Not enough in stock!");
        }else{
            i++;
            proTot = unitPrice * Double.valueOf(proQty_txt.getText());
            finalTot = finalTot + proTot;
            finalQty = finalQty + Integer.valueOf(proQty_txt.getText());
            if(i == 1){
                bill_txt.setText(bill_txt.getText()+"=====GROCERYPALS - Your Daily Pal=====\n" +"Customer: "+cusName_txt.getText()+"     Seller: "+sellName_lbl.getText()+"\n" +"Bill Date: "+billDate_lbl.getText()+"     Bill Time: "+billTime_lbl.getText()+"\n" + "NUM     PRODUCT     U.PRICE     QUANTITY     TOTAL\n" + i +"            "+proName_txt.getText()+"                    "+unitPrice+"            "+proQty_txt.getText()+"                      "+proTot+"\n");
            }else{
                bill_txt.setText(bill_txt.getText()+i +"            "+proName_txt.getText()+"                    "+unitPrice+"            "+proQty_txt.getText()+"                      "+proTot+"\n");
            }
            finalTot_lbl.setText(String.valueOf(finalTot));
            finalQty_lbl.setText(String.valueOf(finalQty));
            remUpdate();
        }
    }//GEN-LAST:event_add_btnMouseClicked

